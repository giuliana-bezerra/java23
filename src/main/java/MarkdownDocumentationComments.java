// Enable JavaDoc documentation comments to be written in Markdown rather than
// solely in a mixture of HTML and JavaDoc @-tags.

/// ## Example Class
/// This class demonstrates the use of **Markdown** in documentation comments.
///
/// ### Features:
/// - Demonstrates Markdown support
/// - Supports standard Markdown syntax
///
/// ```java
/// System.out.println("This is a code block example.");
/// ```
///
/// [Click here for more details](https://openjdk.org/jeps/467)
///
public class MarkdownDocumentationComments {
  public static void main(String[] args) {
    System.out.println("Hello, Markdown in JavaDoc!");
  }
}
